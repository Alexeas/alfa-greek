var p_1 = document.getElementById("p_1").textContent;
var pt_1 = document.getElementById("pt_1").textContent;
$('#p_1').click(function(){
    if (!$(this).data('status')) {
    $(this).html(pt_1);
    $(this).data('status', true);
  }
  else {
    $(this).html(p_1);
    $(this).data('status', false);
  }
});

